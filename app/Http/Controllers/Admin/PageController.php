     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $page = Page::where('registerid', $id)->first();
        $page->nameEn = $request->heading;
        $page->nameArb = $request->headingArb;
        $page->descriptionEn = $request->content;
        $page->descriptionArb = $request->contentArb;

        $page->save();
        return redirect()->route('page.index');
    }

